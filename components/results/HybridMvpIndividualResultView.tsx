import React from 'react'

import { Card } from '@/components/ui/Card'
import { ResultsWorkspaceShell } from '@/components/results/ResultsPrimitives'
import type { IndividualHybridResultReadyData } from '@/lib/server/individual-results'

function formatDate(value: string | null): string | null {
  if (!value) return null

  return new Date(value).toLocaleDateString('en-GB', {
    day: '2-digit',
    month: 'short',
    year: 'numeric',
  })
}

function toPercent(value: number | null | undefined): string {
  if (typeof value !== 'number' || Number.isNaN(value)) return '—'
  return `${Math.round(value * 100)}%`
}

function sectionBlocks(data: IndividualHybridResultReadyData, sectionId: string) {
  return data.hybrid.sections.find((section) => section.id === sectionId)?.blocks ?? []
}

function ReportSection({
  title,
  subtitle,
  blocks,
}: {
  title: string
  subtitle?: string
  blocks: IndividualHybridResultReadyData['hybrid']['sections'][number]['blocks']
}) {
  if (!blocks.length) return null

  return (
    <section className="space-y-3">
      <div className="space-y-1">
        <h2 className="text-lg font-semibold tracking-tight text-textPrimary">{title}</h2>
        {subtitle ? <p className="text-sm text-textSecondary">{subtitle}</p> : null}
      </div>
      <div className="grid gap-3 md:grid-cols-2">
        {blocks.map((block) => (
          <Card key={block.id} className="space-y-2 border-border/80 bg-panel/75">
            <div className="flex items-center justify-between gap-2">
              <h3 className="text-sm font-semibold text-textPrimary">{block.title}</h3>
              {block.value ? <span className="text-xs font-semibold uppercase tracking-[0.12em] text-accent/90">{block.value}</span> : null}
            </div>
            <p className="text-sm leading-6 text-textSecondary">{block.body}</p>
          </Card>
        ))}
      </div>
    </section>
  )
}

export function HybridMvpIndividualResultView({ data }: { data: IndividualHybridResultReadyData }) {
  const strengths = sectionBlocks(data, 'strengths')
  const watchouts = sectionBlocks(data, 'watchouts')
  const developmentFocus = sectionBlocks(data, 'development_focus')
  const domainSummaries = sectionBlocks(data, 'domain_summaries')

  return (
    <ResultsWorkspaceShell
      title={data.hybrid.assessmentMeta.assessmentVersionName ?? 'Individual Intelligence'}
      subtitle={data.hybrid.summary?.text ?? 'Your latest hybrid assessment result is available.'}
      statusLabel="Results ready"
    >
      <Card className="space-y-3 border-border/80 bg-panel/70">
        <div className="flex flex-wrap gap-3 text-sm text-textSecondary">
          {data.hybrid.assessmentMeta.assessmentVersionKey ? <span>Version {data.hybrid.assessmentMeta.assessmentVersionKey}</span> : null}
          {formatDate(data.snapshot.scoredAt) ? <span>Scored {formatDate(data.snapshot.scoredAt)}</span> : null}
          <span>{data.hybrid.rankedSignals.length} ranked signals</span>
        </div>
        {data.hybrid.summary ? (
          <div className="space-y-1">
            <p className="text-[11px] font-semibold uppercase tracking-[0.16em] text-textSecondary">Summary</p>
            <h2 className="text-xl font-semibold tracking-tight text-textPrimary">{data.hybrid.summary.headline}</h2>
          </div>
        ) : null}
      </Card>

      <ReportSection
        title="Strengths"
        subtitle="Top behavioural signals observed in the completed assessment."
        blocks={strengths}
      />
      <ReportSection
        title="Watchouts"
        subtitle="Lower-expression signals worth tracking during execution."
        blocks={watchouts}
      />
      <ReportSection
        title="Development focus"
        subtitle="Priority development area generated by the deterministic hybrid report."
        blocks={developmentFocus}
      />

      <ReportSection
        title="Domain summaries"
        subtitle="Domain-level narrative summary generated from ranked hybrid signals."
        blocks={domainSummaries}
      />

      <section className="space-y-3">
        <div className="space-y-1">
          <h2 className="text-lg font-semibold tracking-tight text-textPrimary">Signal breakdown</h2>
          <p className="text-sm text-textSecondary">Deterministic ranked signal output from your completed assessment payload.</p>
        </div>
        <div className="grid gap-3 md:grid-cols-2 xl:grid-cols-3">
          {data.hybrid.rankedSignals.map((signal) => (
            <Card key={signal.signalId} className="space-y-2 border-border/80 bg-panel/75">
              <p className="text-xs font-semibold uppercase tracking-[0.14em] text-textSecondary">Rank {signal.rank}</p>
              <h3 className="text-base font-semibold text-textPrimary">{signal.signalKey}</h3>
              <div className="space-y-1 text-sm text-textSecondary">
                <p>Normalised score: <span className="font-medium text-textPrimary">{toPercent(signal.normalizedScore)}</span></p>
                <p>Raw score: <span className="font-medium text-textPrimary">{Math.round(signal.rawScore * 100) / 100}</span></p>
              </div>
            </Card>
          ))}
        </div>
      </section>

      {data.hybrid.domainSummaries.length ? (
        <section className="space-y-3">
          <div className="space-y-1">
            <h2 className="text-lg font-semibold tracking-tight text-textPrimary">Domain vector summary</h2>
            <p className="text-sm text-textSecondary">Top signal and weighted distribution per domain vector.</p>
          </div>
          <div className="grid gap-3 md:grid-cols-2 xl:grid-cols-3">
            {data.hybrid.domainSummaries.map((domain, index) => (
              <Card key={`${domain.domainId ?? 'global'}:${index}`} className="space-y-2 border-border/80 bg-panel/75">
                <p className="text-xs font-semibold uppercase tracking-[0.14em] text-textSecondary">{domain.domainId ?? 'Global domain'}</p>
                <p className="text-sm text-textSecondary">Signals: <span className="font-medium text-textPrimary">{domain.signalCount}</span></p>
                <p className="text-sm text-textSecondary">Top signal: <span className="font-medium text-textPrimary">{domain.topSignalId ?? 'N/A'}</span></p>
                <p className="text-sm text-textSecondary">Top score: <span className="font-medium text-textPrimary">{toPercent(domain.topNormalizedScore)}</span></p>
              </Card>
            ))}
          </div>
        </section>
      ) : null}
    </ResultsWorkspaceShell>
  )
}
